"""
Custom Dataset Loader for EXPASS to consume GRENADE outputs.

This file provides a minimal dataset class that EXPASS can use to load
the embeddings and adjacency matrix generated by GRENADE.
"""

import torch
from torch_geometric.data import Data, InMemoryDataset
import numpy as np
import pickle
from pathlib import Path


class GrenadeOutputDataset(InMemoryDataset):
    """
    Loads GRENADE outputs (embeddings + adjacency) as a PyG dataset for EXPASS.
    
    This is a minimal custom dataset that allows EXPASS to work with GRENADE's
    learned graph structure and node embeddings.
    """
    
    def __init__(self, embeddings_path, adjacency_path, labels, 
                 train_mask=None, val_mask=None, test_mask=None):
        """
        Initialize dataset from GRENADE outputs.
        
        Args:
            embeddings_path: Path to .npy file with node embeddings
            adjacency_path: Path to .pkl file with adjacency matrix
            labels: Array of node labels
            train_mask: Boolean mask for training nodes
            val_mask: Boolean mask for validation nodes
            test_mask: Boolean mask for test nodes
        """
        self.embeddings_path = embeddings_path
        self.adjacency_path = adjacency_path
        self.labels = labels
        self._train_mask = train_mask
        self._val_mask = val_mask
        self._test_mask = test_mask
        
        # Load and process data
        self._data_obj = self._load_data()
    
    def _load_data(self):
        """Load GRENADE outputs and create PyG Data object."""
        # Load embeddings
        embeddings = np.load(self.embeddings_path)
        x = torch.FloatTensor(embeddings)
        
        # Load adjacency matrix
        with open(self.adjacency_path, 'rb') as f:
            adj = pickle.load(f)
        
        # Convert adjacency to edge_index
        if hasattr(adj, 'tocoo'):
            adj_coo = adj.tocoo()
            edge_index = torch.LongTensor(np.vstack([adj_coo.row, adj_coo.col]))
        elif isinstance(adj, np.ndarray):
            edge_index = torch.LongTensor(np.array(np.where(adj > 0)))
        else:
            raise ValueError(f"Unknown adjacency format: {type(adj)}")
        
        # Convert labels
        y = torch.LongTensor(self.labels)
        
        # Create masks if not provided
        num_nodes = len(self.labels)
        if self._train_mask is None:
            indices = np.random.permutation(num_nodes)
            train_size = int(num_nodes * 0.6)
            val_size = int(num_nodes * 0.2)
            
            train_mask = torch.zeros(num_nodes, dtype=torch.bool)
            val_mask = torch.zeros(num_nodes, dtype=torch.bool)
            test_mask = torch.zeros(num_nodes, dtype=torch.bool)
            
            train_mask[indices[:train_size]] = True
            val_mask[indices[train_size:train_size + val_size]] = True
            test_mask[indices[train_size + val_size:]] = True
        else:
            train_mask = torch.BoolTensor(self._train_mask)
            val_mask = torch.BoolTensor(self._val_mask)
            test_mask = torch.BoolTensor(self._test_mask)
        
        # Create PyG Data object
        data = Data(
            x=x,
            edge_index=edge_index,
            y=y,
            train_mask=train_mask,
            val_mask=val_mask,
            test_mask=test_mask
        )
        
        return data
    
    def get(self, idx):
        """Get a data object (for compatibility with PyG)."""
        return self._data_obj
    
    def len(self):
        """Return dataset length (for compatibility with PyG)."""
        return 1
    
    @property
    def num_classes(self):
        """Return number of classes."""
        return int(self._data_obj.y.max().item()) + 1
    
    @property
    def num_features(self):
        """Return number of node features."""
        return self._data_obj.x.shape[1]
